package javabasics;

public class ExIfElse {

	public static void main(String[] args) {

		int a;
		int b;
		a = 21;
		b = 20;

		if (a > b) {
			System.out.println("a is greater than b");
		} else {
			System.out.println("b is greater than a");
		}

		System.out.println("Out of If Block");

	}

}
