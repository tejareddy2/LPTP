package javabasics;

public class ExIfElseIf {

	public static void main(String[] args) {

		String breakfast = "Dosa";

		if (breakfast == "Dosa")
			System.out.println("Have Dosa");
		else if (breakfast == "Poori")
			System.out.println("Have Poori");
		else if (breakfast == "Idly")
			System.out.println("Have Idly");
		else
			System.out.println("No Breakfast");
	}

}
